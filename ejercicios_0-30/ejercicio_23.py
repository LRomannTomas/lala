texto = input("Ingrese una palabra: ")
print(f"La primera letra de la palabra que ingresaste es: {texto[0]}")
indice = int(input("Decime un numero positivo que sea menor a la cantidad de caracteres que contiene la palabra que ingresaste: "))
print(f"el carácter del texto ubicado en la posición dada por indice es: {texto[indice]}")