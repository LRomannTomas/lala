primer_numero = float(input("Decime un numero: "))
segundo_numero = float(input("Decime otro numero: "))
suma = primer_numero + segundo_numero
print(f"El resultado de la suma es {suma}")
tercer_numero = float(input("Decime un tercer numero: "))
print(f"El tercer numero multiplicado por la suma de los anteriores es igual a {suma * tercer_numero}")